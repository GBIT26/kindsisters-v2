function CopyButton({ text, label }: { text: string; label: string }) {
  return (
    <div className="relative">
      <div className="flex items-center justify-between mb-2">
        <span className="text-xs font-semibold text-trust-muted uppercase tracking-wider">{label}</span>
        <button
          className="copy-btn text-xs font-medium text-kindness hover:text-kindness-deep transition-colors cursor-pointer"
          data-copy={text}
        >
          Copy
        </button>
      </div>
      <pre className="text-sm text-trust-soft leading-relaxed whitespace-pre-wrap bg-earth rounded-[var(--radius-md)] p-4 max-h-48 overflow-y-auto">{text}</pre>
    </div>
  );
}

